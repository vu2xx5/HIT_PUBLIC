a = 10
b = 5

# a. a cộng b
print("a + b =", a + b)

# b. a trừ b
print("a - b =", a - b)

# c. a nhân b
print("a * b =", a * b)

# d. a chia lấy nguyên b
print("a // b =", a // b)

# e. a mũ b
print("a ** b =", a ** b)

# f. a chia dư b
print("a % b =", a % b)

# g. so sánh a và b (lớn hơn, nhỏ hơn hay bằng)
if a > b:
    print("a lớn hơn b")
elif a < b:
    print("a nhỏ hơn b")
else:
    print("a bằng b")

# h. a AND b
print("a & b =", a & b)

# i. a OR b
print("a | b =", a | b)

# j. a XOR b
print("a ^ b =", a ^ b)

# k. NOT a == b
print("NOT a == b:", not a == b)

# l. a dịch phải 1 đơn vị
print("a >> 1 =", a >> 1)

# m. a dịch trái 1 đơn vị
print("a << 1 =", a << 1)